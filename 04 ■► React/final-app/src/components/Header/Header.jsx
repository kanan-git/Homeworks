// ==============>> import design sheet
import s from './index.module.css'
// ▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬▬
function Header() {

    const [headerMenus, setHeaderMenus] = useState([])

    useEffect(
        async () => {
            const testData = await axios("http://localhost:3001/english")
            // console.log(testData.data.headerListItems)
            setHeaderMenus(testData.data.headerListItems)
        }, []
    )

    return (
        <header className={s.header}>
            <div className={s.header__menulist}>
                {
                    headerMenus.map(
                        (item, index) => <button className={s.header__menulist_items} key={index}> {item} {console.log(item, index)} </button>
                    )
                }
            </div>
        </header>
    )
}

// ==============>> exporting component function Header
export default Header;
